export const getNewsByCategory = (category) => {
  if (category === "All") return newsData;
  return newsData.filter(news => news.category === category);
};

export const getNewsById = (id) => {
  const news = newsData.find(news => news.id === parseInt(id));
  if (!news) return null;
  return {
    ...news,
    slug: news.slug || news.title.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')
  };
};

export const getRelatedNews = (currentNewsId, category, limit = 3) => {
  return newsData
    .filter(news => news.id !== currentNewsId && news.category === category)
    .map(news => ({
      ...news,
      slug: news.slug || news.title.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')
    }))
    .slice(0, limit);
};

export const getFeaturedNews = () => {
  return newsData
    .filter(news => news.featured)
    .map(news => ({
      ...news,
      slug: news.slug || news.title.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')
    }));
};